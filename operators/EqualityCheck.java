package operators;

public class EqualityCheck {

        public static void main(String[] args) {
            int num1 = 10, num2 = 20;
    
            boolean isEqual = (num1 == num2);
    
            System.out.println("Is Equal: " + isEqual);
            boolean isNotEqual = (num1 != num2);
            System.out.println("Is Not Equal: " + isNotEqual);
        }
    }